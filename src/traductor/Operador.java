package traductor;

public class Operador {
	
	private String tipoOp;
	private String codOp;
	
public Operador() {
	this.tipoOp = "";
	this.codOp = "";
}

public String getTipoOp() {
	return tipoOp;
}

public void setTipoOp(String tipoOp) {
	this.tipoOp = tipoOp;
}

public String getCodOp() {
	return codOp;
}

public void setCodOp(String codOp) {
	this.codOp = codOp;
}


	
	

}
